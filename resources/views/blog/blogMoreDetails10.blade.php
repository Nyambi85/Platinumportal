                            <h2>PLATINUM CREDIT LTD WAYAPA MAZINGIRA NA AFYA KIPAUMBELE KATIKA SHULE YA MSINGI GOBA.</h2>
                            <p>Zoezi la upandaji  miti sio la Platinum Credit pekee bali  ni letu sote haya yalisemwa na Ladislaus Mwongerezi
                                Meneja rasilimali watu  wa Platinum Credit Ltd akizindua zoezi la upandaji miti katika shule ya msingi Goba Jijini
                                Dar  es Salaam. </p>
                            <p>Platinum Credit Ltd katika kujali mazingira na afya za wanafuzi waliendesha zoezi  la upandaji miti  katika shule ya msingi 	Goba ambapo walipanda zaidi ya
                                miche 450 ya miti ya matunda ikiwemo miparachichi, michungwa, miembe na mapera ya kisasa.</p>
                            <p>Zoezi hilo lilishuhudiwa na mwenyekiti wa serikali ya mtaa  wa Goba Peter Kiula, Mkurugenzi mkuu Platinum Credit
                                Lazarus Kithuku, Mkurugenzi mtendaji Doris Lyakurwa,  Mwenyekiti kamati ya shule Hezron Baraka, Muwakilishi wa
                                Mwalimu mkuu mwalimu Shaban Kapaya, wafanyakazi wa Platinum Credit, walimu na wanafunzi wa shule ya msingi Goba.</p>

                            <p>Meneja rasilimali watu wa Platinum Credit Ladislaus Mwongerezi alizungumza  kwaniaba ya Mkurugenzi wa taasisi hiyo na
                                kusema kuwa wamefika hapo kuongeza nguvu ya malezi kwa kizazi cha Tanzania.</p>
                            <p>“Tupo unapo tuhitaji kama kauli mbiu yetu inavyosema. Tumefika hapa kuongeza nguvu katika kulea kizazi cha Tanzania kwa kuwapatia lishe bora kwa kupanda miti
                                ya matunda ambayo pia ni chachu katika kutunza mazingira” alisema.</p>
                            <p>Ladsilaus Mwongerezi aliwatoa wasiwasi walimu na wanafunzi wa shule
                                hiyo kwa kuwambia kuwa miti hiyo ni ya muda mfupi, itatoa maua na kuzaa matunda mapema ikitunzwa vizuri.</p>
                            <p> “Hii ni miti ya matunda ni ya kisasa huchukuwa muda mfupi tu kutoa maua na kuleta matunda kama itatunzwa vizuri
                                hivyo naomba tushirikiane kuitunza.” alisema.</p>
                            <p>Kwa upande wa mwenyekiti wa Serikali ya mtaa aliwataka wanafunzi watambue umuhimu wa miti hiyo na waitunze kwa
                                pamoja.</p>
                            <p>“Miti inafaida kubwa na bahati nzuri tumeletewa miti ya matunda, wanafuzi miti hii tukiipanda isife hata mmoja, tuweke maji, tuweke mbolea tutunze mazingira.”alisisitiza.
                                Vilevile Meneja rasilimali watu wa Platinum Credit alitumia nafasi hiyo kushukuru uongozi wa shule na serikali ya mtaa wa Goba , walimu na wanafunzi kwa ujumla kwa ushirikiano waliowaonyesha tangu mwanzo mpaka mwisho wa zoezi hilo.</p>

                            <p>“Tunashukuru kwa kutupokea na kutupanafasi ya kupanda miti hapa, mapokezi yamekuwa mazuri  tangu mwanzo mpaka mwisho wa zoezi hili, tunamshukuru mwenyekiti wa serikali ya mtaa kuwa pamoja nasi hapa.
                                ”alishukuru.</p>

                            <p>Kwa upande wa shule ya msingi Goba Mwaalimu wa mazingira Sebarua Abeid alitoa neno la shukrani na kugusia baadhi ya changamoto ambazo zinawakabili na zinahitaji utatuzi.</p>

                            <p>“Mimi kama mwalimu wa mazingira natambua umuhimu wa miti, nawashukuru Platinum Credit kwa kuiona Goba na siyo shule nyingine. Tunashukuru na tuaahidi kuitunza na kuilinda miti hii, japo kuwa bado tunahitaji msaada kwani tuna changamoto ya maji na uzio tunawaomba wanapoenda huko waendelee kutuifikiria.
                                ” Alisema.</p>

