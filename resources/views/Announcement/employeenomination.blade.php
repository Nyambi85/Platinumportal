                        <blockquote>
                            <p>The end result being to encourage, inspire further staff engagement, better individual performance,
                                better business results, loyalty and retention of important and skilled staff and above all build a supportive work
                                environment and a sense of ownership of the work place.
                            </p>
                        </blockquote>

                        <span><b>2. Period:</b></span>
                        <blockquote>
                            <p>Every Month.
                            </p>
                        </blockquote>

                        <span><b>3. Nomination Committee:</b></span>
                        <blockquote>
                            <p>This shall be constituted of not less than 3 staff at a senior managerial level. They shall handle the functioning of the reward program.</p>
                        </blockquote>

                        <span><b>4. Nomination Criteria:</b></span>
                        <blockquote>
                            <ul>
                                <li><p><b>Teamwork</b>,
                                        With examples we shall be required to describe how the employee has demonstrated excellence in this area.</p></li>
                                <li><p><b>Performance commitment and productivity</b>,
                                        With examples we shall be required to describe how the employee has demonstrated excellence in this area.</p></li>
                                <li><p><b>General conduct/behavior at work</b>,
