                            To achieve this performance, recognition award program is put in place.</p>

                        <p>Normally such a program is structured to enable management and fellow employees to recognize and reward outstanding achievement of employees and to stimulate the
                            potential of all employees to recognize and reward outstanding achievements of an employee and to stimulate the potential of all employees to achieve higher
