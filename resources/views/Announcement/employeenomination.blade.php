                                <li><p><b>Customer Service</b>,
                                        With examples we shall be required to describe how the employee has demonstrated excellence in this area.</p></li>
                                <li><p><b>Outstanding accomplishments</b>,
                                        Mention contributions an employee has made which go beyond an employee’s job duties for improvement of the company productivity.</p></li>
                            </ul>
                        </blockquote>

                        <span><b>5. Types of rewards:</b></span>
                        <blockquote>
                            <p>This shall be announced on every month.</p>
                        </blockquote>

                        <span><b>6. Nomination process flow:</b></span>

                        <blockquote>
                            <ul>
                                <li><p>On the last working day of the nomination month, nomination proposal forms shall
                                        be duly filled by a staff wishing to nominate his/her fellow staff as a best performer of the month.</p></li>
                                <li><p>The Nomination committee shall convene a meeting on the second working day of the following month to nominate/select the winner.</p></li>
                                <li><p>This committee meets and reviews all the nominations and boils down to pick the appropriate employee
                                    for the upcoming month for the said category. A follow-up email is sent if required.</p></li>
                                <li><p>On the third working day of the month following nomination month,
